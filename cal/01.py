# 1. 변수 정의
num1 = 15
num2 = 4

# 2. 덧셈 (Addition)
sum_result = num1 + num2
print(f"덧셈 결과: {num1} + {num2} = {sum_result}")
# 출력: 덧셈 결과: 15 + 4 = 19

# 3. 뺄셈 (Subtraction)
difference_result = num1 - num2
print(f"뺄셈 결과: {num1} - {num2} = {difference_result}")
# 출력: 뺄셈 결과: 15 - 4 = 11

# 4. 곱셈 (Multiplication)
product_result = num1 * num2
print(f"곱셈 결과: {num1} * {num2} = {product_result}")
# 출력: 곱셈 결과: 15 * 4 = 60

# 5. 나눗셈 (Division - float)
division_result = num1 / num2
print(f"나눗셈 결과 (실수): {num1} / {num2} = {division_result}")
# 출력: 나눗셈 결과 (실수): 15 / 4 = 3.75

# 6. 정수 나눗셈 (Floor Division - 몫)
floor_result = num1 // num2
print(f"정수 나눗셈 결과 (몫): {num1} // {num2} = {floor_result}")
# 출력: 정수 나눗셈 결과 (몫): 15 // 4 = 3

# 7. 나머지 연산 (Modulus)
remainder_result = num1 % num2
print(f"나머지 연산 결과: {num1} % {num2} = {remainder_result}")
# 출력: 나머지 연산 결과: 15 % 4 = 3
